// 5. Remote DB Connector (Stub)
//
// Many relational databases such as PostgreSQL or MySQL are not accessible
// directly from the browser due to cross‑origin restrictions and the need
// for authentication.  This connector provides a placeholder illustrating
// how one might bridge to a server‑side API that executes queries on the
// user's behalf.  In order to work fully the agent would need to supply
// appropriate connection strings and implement an API endpoint to relay
// queries.  Here we assume there exists an endpoint at /api/db/query.

class RemoteDbConnector implements IDatabaseConnector {
  id: string;
  name: string;
  type: string;
  status: DbStatus = 'disconnected';
  error?: any;
  private endpoint: string;

  constructor(id: string, name: string, type: string, endpoint: string) {
    this.id = id;
    this.name = name;
    this.type = type;
    this.endpoint = endpoint;
  }

  async connect() {
    // For remote connectors we assume connectivity is always available and
    // mark the status as connected.  In a real implementation this could
    // perform a health check against the endpoint.
    this.status = 'connected';
  }

  async query(sql: string): Promise<QueryResult> {
    try {
      const res = await fetch(this.endpoint, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ query: sql }),
      });
      if (!res.ok) throw new Error(`Remote DB error: ${res.statusText}`);
      const json = await res.json();
      return json as QueryResult;
    } catch (e) {
      this.status = 'error';
      this.error = e;
      throw e;
    }
  }

  async insert(table: string, data: any): Promise<void> {
    await this.query(`INSERT INTO ${table} (${Object.keys(data).join(', ')}) VALUES (${Object.values(data).map((v) => `'${v}'`).join(', ')});`);
  }

  async meta(): Promise<{ tables: number; rows: number; sizeMB: number }> {
    // Endpoint must implement a metadata route; we return placeholder values
    return { tables: 0, rows: 0, sizeMB: 0 };
  }
}

