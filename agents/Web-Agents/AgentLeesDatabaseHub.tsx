// 6. Vector Store Connector (Stub)
//
// Vector databases store high‑dimensional vectors for similarity search and
// retrieval augmented generation.  Implementing a full vector database in
// browser would be out of scope here, but this stub demonstrates the API.

class VectorDbConnector implements IDatabaseConnector {
  id = 'vectorDb';
  name = 'Vector Store';
  type = 'VectorDB';
  status: DbStatus = 'connected';
  error?: any;
  private store: Record<string, number[]> = {};

  async connect() {
    // In this stub there is nothing to initialise.
    this.status = 'connected';
  }

  async query(sql: string): Promise<QueryResult> {
    // We interpret the SQL as a similarity search when it matches the
    // pattern SELECT id, similarity FROM vectors WHERE MATCH('...')
    const match = /match\s*\(\s*['"]([^'"]+)['"]\s*\)/i.exec(sql);
    if (match) {
      const vectorString = match[1];
      const queryVector = vectorString.split(',').map((v) => parseFloat(v));
      const results: [string, number][] = [];
      for (const [id, vec] of Object.entries(this.store)) {
        // Compute cosine similarity
        let dot = 0;
        let normA = 0;
        let normB = 0;
        for (let i = 0; i < vec.length; i++) {
          dot += vec[i] * queryVector[i];
          normA += vec[i] ** 2;
          normB += queryVector[i] ** 2;
        }
        const sim = dot / (Math.sqrt(normA) * Math.sqrt(normB));
        results.push([id, sim]);
      }
      // Sort by similarity descending
      results.sort((a, b) => b[1] - a[1]);
      return { columns: ['id', 'similarity'], rows: results };
    }
    return { columns: [], rows: [] };
  }

  async insert(table: string, data: any): Promise<void> {
    // Expect data = { id: string, vector: number[] }
    this.store[data.id] = data.vector;
  }

  async meta(): Promise<{ tables: number; rows: number; sizeMB: number }> {
    const count = Object.keys(this.store).length;
    // Roughly estimate size as (vector length * 8 bytes) per entry
    let totalDims = 0;
    for (const vec of Object.values(this.store)) {
      totalDims += vec.length;
    }
    const sizeMB = (totalDims * 8) / 1024 / 1024;
    return { tables: 1, rows: count, sizeMB };
  }
}

