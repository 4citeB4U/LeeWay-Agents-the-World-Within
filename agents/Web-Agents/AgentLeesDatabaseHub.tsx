// -----------------------------------------------------------------------------
// 1. Type Definitions
//
// These definitions describe a generic interface for different database
// connectors.  Each connector exposes a name, human‑readable type and a
// status flag along with asynchronous methods to establish a connection,
// execute arbitrary SQL queries, insert data and return basic metadata.  In
// keeping with the goals of this project the interface is intentionally
// generic so that new backends can be plugged in without modifying the
// surrounding UI.

export type DbStatus = 'disconnected' | 'connecting' | 'connected' | 'error';

export interface QueryResult {
  columns: string[];
  rows: any[][];
}

export interface IDatabaseConnector {
  /** Unique identifier for the database instance. */
  id: string;
  /** Human‑readable name for display in the UI. */
  name: string;
  /** Short type description (e.g. "IndexedDB", "SQL.js", "DuckDB"). */
  type: string;
  /** Current connection state. */
  status: DbStatus;
  /** Optional error message if `status` is set to "error". */
  error?: any;
  /** Establish a connection and populate internal caches. */
  connect: () => Promise<void>;
  /** Execute an SQL statement and return a tabular result. */
  query: (sql: string) => Promise<QueryResult>;
  /** Insert or update data into a table.  Accepts arbitrary JSON values. */
  insert: (table: string, data: any) => Promise<void>;
  /** Return high‑level statistics such as number of tables and approximate size. */
  meta: () => Promise<{ tables: number; rows: number; sizeMB: number }>;
}

// -----------------------------------------------------------------------------
// 2. IndexedDB Cache Connector
//
// This connector implements a simple key‑value store using IndexedDB.  It
// primarily serves as a persistent cache for query results and other
// transient data generated by the agent.  Because IndexedDB stores objects
// natively and supports large quotas, it is ideal for storing serialised
// representations of query results or small blobs without incurring the
// limitations of localStorage【261891055882733†L55-L78】.

interface CacheDB extends DBSchema {
  queries: {
    key: string;
    value: any;
  };
}

class IndexedDbConnector implements IDatabaseConnector {
  id = 'indexedDb';
  name = 'IndexedDB Cache';
  type = 'IndexedDB';
  status: DbStatus = 'disconnected';
  private dbPromise!: Promise<IDBPDatabase<CacheDB>>;
  error?: any;

  async connect() {
    this.status = 'connecting';
    try {
      this.dbPromise = openDB<CacheDB>('agent-lee-cache', 1, {
        upgrade(db) {
          if (!db.objectStoreNames.contains('queries')) {
            db.createObjectStore('queries');
          }
        },
      });
      await this.dbPromise;
      this.status = 'connected';
    } catch (e) {
      this.status = 'error';
      this.error = e;
    }
  }

  /**
   * Execute a pseudo‑SQL query against the cache.  Because IndexedDB
   * doesn't support SQL natively, this method accepts limited commands of
   * the form "SELECT * FROM queries WHERE key = '...';" to retrieve a
   * previously cached result.  Any other statement returns an empty result.
   */
  async query(sql: string): Promise<QueryResult> {
    const db = await this.dbPromise;
    const trimmed = sql.trim().toLowerCase();
    const match = /select\s+\*\s+from\s+queries\s+where\s+key\s*=\s*['"]([^'"]+)['"]/.exec(trimmed);
    if (match) {
      const key = match[1];
      const val = await db.get('queries', key);
      if (val !== undefined) {
        return { columns: ['result'], rows: [[val]] };
      }
    }
    // Return empty when no pattern matched
    return { columns: [], rows: [] };
  }

  /** Store an arbitrary JSON value under the provided table/key.  We treat
   * the table name as the key for simplicity; more sophisticated schemas
   * can be layered on top if needed. */
  async insert(table: string, data: any): Promise<void> {
    const db = await this.dbPromise;
    await db.put('queries', data, table);
  }

  async meta(): Promise<{ tables: number; rows: number; sizeMB: number }> {
    const db = await this.dbPromise;
    let count = 0;
    let bytes = 0;
    const tx = db.transaction('queries');
    const store = tx.store;
    for await (const cursor of store) {
      count++;
      const value = cursor.value;
      bytes += JSON.stringify(value).length;
    }
    return { tables: 1, rows: count, sizeMB: bytes / 1024 / 1024 };
  }
}

// -----------------------------------------------------------------------------
// 3. SQL.js Connector
//
// sql.js compiles SQLite to WebAssembly so that it runs entirely within the
// browser.  The library provides a familiar SQL interface but, by default,
// keeps the entire database in memory.  To persist the state across
// sessions the database must be serialised and saved into a persistent store
// such as IndexedDB【261891055882733†L55-L78】.  This connector encapsulates
// those details and exposes a unified query API.

interface SqlJsModule {
  Database: any;
  FS: any;
  ccall: any;
  ERRNO_CODES: any;
}

class SqlJsConnector implements IDatabaseConnector {
  id = 'sqlJs';
  name = 'SQLite (sql.js)';
  type = 'SQL.js';
  status: DbStatus = 'disconnected';
  error?: any;
  private SQL: SqlJsModule | null = null;
  private db: any = null;
  private cache: IndexedDbConnector;

  constructor(cache: IndexedDbConnector) {
    this.cache = cache;
  }

  async connect() {
    this.status = 'connecting';
    try {
      // Dynamically import the sql.js library.  We locate the WASM file on
      // a CDN so that bundlers do not need to embed it.  If this import
      // fails the connection will remain in the "error" state.
      // @ts-ignore
      const initSqlJs = (await import('https://cdnjs.cloudflare.com/ajax/libs/sql.js/1.8.0/sql-wasm.js')).default;
      const SQL = await initSqlJs({
        locateFile: (file: string) => `https://cdnjs.cloudflare.com/ajax/libs/sql.js/1.8.0/${file}`,
      });
      this.SQL = SQL;
      this.db = new SQL.Database();
      this.status = 'connected';
    } catch (e) {
      this.status = 'error';
      this.error = e;
    }
  }

  async query(sql: string): Promise<QueryResult> {
    if (!this.db) {
      throw new Error('SQL.js database is not initialised');
    }
    const trimmed = sql.trim().toLowerCase();
    // If the statement is a SELECT we attempt to look up a cached result
    // first.  Otherwise we execute it directly.
    if (trimmed.startsWith('select')) {
      const cacheKey = `sqljs:${sql}`;
      const cached = await this.cache.query(`SELECT * FROM queries WHERE key = '${cacheKey}'`);
      if (cached.rows.length > 0) {
        const [result] = cached.rows[0];
        return result as QueryResult;
      }
    }
    try {
      const result = this.db.exec(sql);
      if (result.length === 0) {
        return { columns: [], rows: [] };
      }
      const res = result[0];
      const queryResult: QueryResult = { columns: res.columns, rows: res.values };
      // Persist the result into the cache for future lookups
      if (trimmed.startsWith('select')) {
        const cacheKey = `sqljs:${sql}`;
        await this.cache.insert(cacheKey, queryResult);
      }
      return queryResult;
    } catch (e) {
      throw e;
    }
  }

  async insert(table: string, data: any): Promise<void> {
    if (!this.db) throw new Error('SQL.js database is not initialised');
    // Naively create a table if it does not exist.  Keys of the data object
    // define column names and values are inserted directly.  Note that this
    // does not handle types or escaping and is intended solely as a simple
    // demonstration.
    const cols = Object.keys(data);
    const vals = Object.values(data);
    const colDefs = cols.map((c) => `${c} TEXT`).join(', ');
    const placeholders = vals.map(() => '?').join(', ');
    this.db.exec(`CREATE TABLE IF NOT EXISTS ${table} (${colDefs});`);
    const stmt = this.db.prepare(`INSERT INTO ${table} (${cols.join(', ')}) VALUES (${placeholders});`);
    stmt.run(...vals);
    stmt.free();
  }

  async meta(): Promise<{ tables: number; rows: number; sizeMB: number }> {
    if (!this.db) throw new Error('SQL.js database is not initialised');
    // Query sqlite_master to count tables
    const tableResult = this.db.exec(`SELECT COUNT(*) AS count FROM sqlite_master WHERE type='table' AND name NOT LIKE 'sqlite_%';`);
    const tables = tableResult?.[0]?.values?.[0]?.[0] ?? 0;
    // Estimate rows as the sum of row counts from all tables
    let rows = 0;
    const tablesList = this.db.exec(`SELECT name FROM sqlite_master WHERE type='table' AND name NOT LIKE 'sqlite_%';`);
    for (const entry of tablesList?.[0]?.values ?? []) {
      const tableName = entry[0];
      const countRes = this.db.exec(`SELECT COUNT(*) AS c FROM ${tableName};`);
      rows += countRes?.[0]?.values?.[0]?.[0] ?? 0;
    }
    // Serialise database to determine size
    const binary = this.db.export();
    const sizeMB = binary.length / 1024 / 1024;
    return { tables, rows, sizeMB };
  }
}

// -----------------------------------------------------------------------------
// 4. DuckDB (WASM) Connector
//
// DuckDB is a fast analytical database.  When compiled to WebAssembly it can
// run in the browser with no native dependencies【874286236594197†L463-L470】.
// Because the library isn't bundled with this project, we load it via a
// dynamic import from a CDN.  This connector initialises the DuckDB engine
// asynchronously and exposes the same query API as other connectors.

class DuckDbConnector implements IDatabaseConnector {
  id = 'duckDb';
  name = 'DuckDB (WASM)';
  type = 'DuckDB';
  status: DbStatus = 'disconnected';
  error?: any;
  private db: any;
  private worker: any;

  async connect() {
    this.status = 'connecting';
    try {
      // Dynamically import duckdb-wasm from the jsDelivr CDN.  The +esm
      // suffix returns an ES module that can be imported natively.  See
      // https://cdn.jsdelivr.net/npm/@duckdb/duckdb-wasm/+esm for details.
      // @ts-ignore
      const duckdb = await import('https://cdn.jsdelivr.net/npm/@duckdb/duckdb-wasm@1.25.1/+esm');
      // Select a bundle that contains both the main module and a worker.
      const bundles = duckdb.getJsDelivrBundles();
      // Choose the first bundle; in practice you might prefer the mvp or eh bundle.
      const bundle = bundles[0];
      // Create a new asynchronous worker.  This runs queries off the main thread.
      const worker = new duckdb.AsyncDuckDBWorker();
      const db = new duckdb.DuckDB(worker);
      await db.instantiate(bundle.mainModule, bundle.pthreadWorker);
      // Open an in‑memory database; persistent storage would require OPFS.
      const conn = await db.connect();
      this.worker = worker;
      this.db = conn;
      this.status = 'connected';
    } catch (e) {
      this.status = 'error';
      this.error = e;
    }
  }

  async query(sql: string): Promise<QueryResult> {
    if (!this.db) throw new Error('DuckDB database not initialised');
    const result = await this.db.query(sql);
    return { columns: result.columns, rows: result.toArray() };
  }

  async insert(table: string, data: any): Promise<void> {
    if (!this.db) throw new Error('DuckDB database not initialised');
    // Create table if it doesn't exist
    const cols = Object.keys(data);
    const colDefs = cols.map((c) => `${c} TEXT`).join(', ');
    await this.db.query(`CREATE TABLE IF NOT EXISTS ${table} (${colDefs});`);
    const values = cols.map((c) => `'${String(data[c]).replace(/'/g, "''")}'`).join(', ');
    await this.db.query(`INSERT INTO ${table} (${cols.join(', ')}) VALUES (${values});`);
  }

  async meta(): Promise<{ tables: number; rows: number; sizeMB: number }> {
    if (!this.db) throw new Error('DuckDB database not initialised');
    const res = await this.db.query(`SELECT COUNT(*) AS count FROM information_schema.tables WHERE table_schema='main';`);
    const tables = res.get(0).get('count');
    const res2 = await this.db.query(`SELECT SUM(reltuples) AS rows FROM pg_catalog.pg_class WHERE relname IN (SELECT table_name FROM information_schema.tables WHERE table_schema='main');`);
    const rows = res2.get(0).get('rows') ?? 0;
    // DuckDB doesn't expose file size for in‑memory DB; we report 0
    return { tables, rows, sizeMB: 0 };
  }
}

